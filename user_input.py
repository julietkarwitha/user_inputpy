# user_input.py

# Asking for user's name and storing it in the variable 'name'
name = input("What is your name? ")

# Asking for user's age and storing it in the variable 'age'
age = input("How old are you? ")

# Asking for user's location and storing it in the variable 'location'
location = input("Where are you located? ")

# Printing personalized message
print(f"Hello {name}, you are {age} years old and live in {location}.")